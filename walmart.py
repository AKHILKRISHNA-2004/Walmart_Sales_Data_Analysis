import csv
import random
from datetime import datetime, timedelta

locations = ["Jakarta", "Bali", "Surabaya", "Bandung", "Medan"]
num_taxpayers = 100
filename = "synthetic_tax_data.csv"

with open(filename, "w", newline="") as f:
    writer = csv.writer(f)
    writer.writerow(["taxpayer_id","transaction_id","amount","location","timestamp"])

    for t in range(1, num_taxpayers+1):
        taxpayer_id = f"T{t:03d}"
        num_tx = random.randint(5, 10)
        for tx in range(1, num_tx+1):
            transaction_id = f"tx{tx:03d}"
            # Amount: mostly small, some high for suspicious cases
            if random.random() < 0.1:
                amount = random.randint(50000, 200000)  # suspicious high
            else:
                amount = round(random.uniform(1.0, 10000.0), 2)
            location = random.choice(locations)
            timestamp = (datetime(2025,6,1,8,0,0) + timedelta(hours=random.randint(0,200))).isoformat()
            writer.writerow([taxpayer_id, transaction_id, amount, location, timestamp])

print(f"Synthetic dataset generated: {filename}")

